"""
Operadores Aritimeticos
+. adição,
-: Subtração,
*: Multiplicação,
/: Divisão Normal,
//: Divisão inteira,
**: potenciação 2²,
%,
(): Alterar precedencia
"""

print('Multiplicação 10 * 10 =', 10 * 10)
print('Adição 10 + 10 = ', 10 + 10)
print('Subtração 10 - 5 = ', 10 - 5)
print('Divisão 10 / 2 = ', 10 / 2)
print('Multiplicação de dados', 5 * "A") ##repeti o A 5 vezes
print('Maria' + 'de fátima' + 'da silva') ##repeti o A 5 vezes
print('Maria' + 'de fátima' + 'da silva' + str(32))  ##O + também serve para concatenar
print(10 // 3)  #Retorna o resultado em inteiro
print(10 / 3)    #Retorna o resultado em ponto flutuante
print(10 ** 2)  #Potenciação
print(10 % 5)  #Reto da divisão

